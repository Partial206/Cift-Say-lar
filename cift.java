public class cift {
    public static void main(String[] args) {
        for(int i=2;i<=100;i+=2){ /* i yi 2 olarak belirler ve 100 e ulaşana kadar 2 ekler */
            System.out.println(i); /* i 100 e ulaşana kadar çift sayıları yazar*/
        }
        System.out.println("Bitti."); /* 100 e gelindiğinde "bitti" yazdırır*/
    }
}
